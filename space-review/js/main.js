//Create and array of numbers. Sum all of the numbers. Alert the sum.
function sumOfArray(nums) {
    let sums = 0
    for(let i=0; i<nums.length; i++) {
        sums = sums + nums[i]
    }
    return sums
}
const result = sumOfArray([3,6,9,15])
console.log(result)

